// ============================================================
// 톤 라우팅 (결혼 스타일 × 하객 연령 비중 → MentTone)
// ============================================================

export const mentToneFor = (weddingStyle: WeddingStyle, ageMix: GuestAgeMix): MentTone => {
  // 스몰/셀프 웨딩: 격식보다 따뜻함이 기본 톤
  if (weddingStyle === "small" || weddingStyle === "self") {
    if (ageMix === "elder" || ageMix === "family") return "warm_family";
    return "casual_peer";
  }
  // 일반 결혼식: 어르신·회사 손님 비중에 따라 격식 결정
  if (ageMix === "elder") return "formal_elder";
  if (ageMix === "work") return "formal_work";
  if (ageMix === "family") return "warm_family";
  if (ageMix === "peer") return "casual_peer";
  return "formal_elder"; // balanced → formal_elder (가장 안전한 기본)
};

// ============================================================
// 변수 치환: {{groom}} / {{bride}} / {{venue}}
// ============================================================

export const formatPhrase = (
  phrase: string,
  vars: { groom?: string; bride?: string; venue?: string },
): string => {
  return phrase
    .replace(/\{\{groom\}\}/g, vars.groom || "신랑")
    .replace(/\{\{bride\}\}/g, vars.bride || "신부")
    .replace(/\{\{venue\}\}/g, vars.venue || "피로연장");
};
