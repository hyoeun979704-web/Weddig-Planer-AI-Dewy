// 같은 데이터를 (1) 크롤러용 edge SSR(api/guide.ts)과 (2) 사용자용 React 페이지
// (src/pages/Guide.tsx)가 함께 사용한다 → 본문/구조화 데이터 불일치 방지.
//
// 목적: "결혼어플추천 / 결혼 준비 앱 추천 / AI 웨딩플래너 앱" 같은 질문에
// Dewy 공식 페이지가 가장 인용하기 좋은 답변형 문서가 되게 한다.
// 작성 원칙: 각 페이지는 "정의형 즉답 → 충분한 일반 설명(색인 가치) → Dewy 연결" 순서.
// thin content(색인 보류) 방지를 위해 각 페이지는 구체적 정보 + 가이드 상호링크를 둔다.

export interface GuideSection {
  h2: string;
  body?: string;
  list?: string[];
}

export interface GuideFaq {
  q: string;
  a: string;
}

export interface GuideLink {
  label: string;
  /** 앱 내부 경로(절대). 내부 링크 = SEO + 전환 동선. */
  path: string;
}

export interface GuideTableRow {
  /** 결혼 준비 앱 선택 기준 */
  criterion: string;
  /** 왜 중요한가 */
  why: string;
  /** Dewy 기능 연결 */
  dewy: string;
}

export interface AeoGuide {
  slug: string;
  /** <title> / og:title */
  title: string;
  metaDescription: string;
  h1: string;
  /** 정의형 즉답문(일반 정의 + Dewy 연결). AI가 그대로 인용하기 좋은 한두 문장. */
  answer: string;
  sections: GuideSection[];
  table?: { headers: [string, string, string]; rows: GuideTableRow[] };
  faqs: GuideFaq[];
  /** 앱 기능 바로가기(전환 동선) */
  related: GuideLink[];
  /** 관련 가이드(토픽 클러스터 상호링크 — 색인·권위 강화) */
  relatedGuides: GuideLink[];
  /** BreadcrumbList 의 현재 페이지 이름 */
  breadcrumbName: string;
  /** 최종 업데이트(신선도·E-E-A-T 신호) */
  updated: string;
}

