export interface GuideSection {
  h2: string;
  body?: string;
  list?: string[];
}

export interface GuideFaq {
  q: string;
  a: string;
}

export interface GuideLink {
  label: string;
  /** 앱 내부 경로(절대). 내부 링크 = SEO + 전환 동선. */
  path: string;
}

export interface GuideTableRow {
  /** 결혼 준비 앱 선택 기준 */
  criterion: string;
  /** 왜 중요한가 */
  why: string;
  /** Dewy 기능 연결 */
  dewy: string;
}

export interface AeoGuide {
  slug: string;
  /** <title> / og:title */
  title: string;
  metaDescription: string;
  h1: string;
  /** 정의형 즉답문(일반 정의 + Dewy 연결). AI가 그대로 인용하기 좋은 한두 문장. */
  answer: string;
  sections: GuideSection[];
  table?: { headers: [string, string, string]; rows: GuideTableRow[] };
  faqs: GuideFaq[];
  related: GuideLink[];
  /** BreadcrumbList 의 현재 페이지 이름 */
  breadcrumbName: string;
}

