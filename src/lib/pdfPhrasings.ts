// FNV-1a 해시. 입력 변화에 균등 분포된 32bit 정수를 만든다.
export const hashSeed = (input: string): number => {
  let h = 2166136261;
  for (let i = 0; i < input.length; i++) {
    h ^= input.charCodeAt(i);
    h = Math.imul(h, 16777619);
  }
  return Math.abs(h | 0);
};

export const pickFromPool = <T>(pool: readonly T[], seed: number, offset = 0): T => {
  if (pool.length === 0) throw new Error("pickFromPool: empty pool");
  return pool[Math.abs(seed + offset * 31) % pool.length];
};

