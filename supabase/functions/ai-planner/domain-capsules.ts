interface CapsuleContext {
  marital_history: string | null;
  pregnant: boolean | null;
}

/**
 * 사용자 컨텍스트(weddingSettings)에 따라 조건부 캡슐을 조립한다.
 * 빈 문자열이면 system prompt 에 아무것도 추가하지 않음.
 */
export function buildConditionalCapsules(ws: CapsuleContext | null | undefined): string {
  if (!ws) return "";
  const parts: string[] = [];
  if (ws.marital_history === "remarriage") parts.push(REMARRIAGE_CAPSULE);
  if (ws.pregnant) parts.push(PREGNANCY_CAPSULE);
  if (parts.length === 0) return "";
  return `\n\n${parts.join("\n\n")}`;
}
